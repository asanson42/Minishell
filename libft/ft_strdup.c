#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		len_src;
	char	*s2;

	i = 0;
	len_src = (int)ft_strlen((char *)s1);
	s2 = malloc(sizeof(char) * len_src + 1);
	if (!s2)
		return (NULL);
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
