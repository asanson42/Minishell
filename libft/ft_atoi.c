#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	c;
	int	n;

	i = 0;
	c = 1;
	n = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			c = c * -1;
		i++;
	}
	while (str[i] && (str[i] >= 48 && str[i] <= 57))
		n = (n * 10) + (str[i++] - 48);
	return (n * c);
}
